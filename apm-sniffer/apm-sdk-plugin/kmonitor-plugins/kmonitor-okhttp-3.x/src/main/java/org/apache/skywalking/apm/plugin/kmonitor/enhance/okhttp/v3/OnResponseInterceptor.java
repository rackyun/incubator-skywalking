package org.apache.skywalking.apm.plugin.kmonitor.enhance.okhttp.v3;

import okhttp3.HttpUrl;
import okhttp3.Request;
import okhttp3.Response;
import org.apache.skywalking.apm.agent.core.plugin.interceptor.enhance.EnhancedInstance;
import org.apache.skywalking.apm.agent.core.plugin.interceptor.enhance.InstanceMethodsAroundInterceptor;
import org.apache.skywalking.apm.agent.core.plugin.interceptor.enhance.MethodInterceptResult;
import org.apache.skywalking.apm.plugin.kmonitor.common.MonitorContext;

import java.lang.reflect.Method;

/**
 * {@link OnResponseInterceptor} validate the response code if it is great equal than 400. if so. the transaction status
 * chang to `error`, or do nothing.
 *
 * @author zhangxin
 */
public class OnResponseInterceptor implements InstanceMethodsAroundInterceptor {
    @Override
    public void beforeMethod(EnhancedInstance objInst, Method method, Object[] allArguments, Class<?>[] argumentsTypes,
        MethodInterceptResult result) throws Throwable {
        Response response = (Response)allArguments[1];
        Request request = response.request();
        HttpUrl requestUrl = request.url();
        if (response.code() >= 400) {
            MonitorContext.getMetrics().probe().tag("method", request.method()).tag("target.host", requestUrl.host())
                .counter("okhttp.error").inc();
        }
    }

    @Override
    public Object afterMethod(EnhancedInstance objInst, Method method, Object[] allArguments, Class<?>[] argumentsTypes,
        Object ret) throws Throwable {
        return ret;
    }

    @Override public void handleMethodException(EnhancedInstance objInst, Method method, Object[] allArguments,
        Class<?>[] argumentsTypes, Throwable t) {
    }
}
